function calculateStar(current, value) {
  return current <= value
    ? star.full
    : current - value < 1 && current - value > 0
    ? star.half
    : star.empty;
}

export default function Star({ value, setValue, disabled = false, className }) {
  return (
    <div className={"flex gap-1/2 " + className}>
      {value && value > 0
        ? [...Array(5).keys()].map((row, i) => (
            <button
              key={i}
              disabled={disabled}
              onClick={() => setValue(row + 1)}
            >
              <>{calculateStar(row + 1, value)}</>
            </button>
          ))
        : [...Array(5)].map((row) => star.empty)}
    </div>
  );
}
